def sgd(params, lr, batch_size):
    with torch.no_grad():
        for param in params:
            # print(id(param))
            #这里有个坑，这里tensor应该是重载 ”-=“还有很多其他符号被重载；这里“-=”不会改变内存地址
            param -= lr * param.grad / batch_size
            # print(id(param))
            param.grad.zero_()
